package com.java.literals;

public class BinaryLiteral
{
    public static void main(String[] args)
    {
        //From jdk 1.7 onwards two enhancement in literals
        /*  1.Binary Literal:
              The literal which is pre fixed with 0b or OB with integral value known as Binary
              Literal
         */

        int x=0B1111;
        System.out.println(x);

        int y=0b1010;
        System.out.println(y);
    }
}
